// Generate attendance data for current month
const generateAttendanceForMonth = (year, month) => {
  const daysInMonth = new Date(year, month, 0).getDate();
  const records = {};
  for (let day = 1; day <= daysInMonth; day++) {
    const date = new Date(year, month - 1, day);
    const dayOfWeek = date.getDay();
    if (dayOfWeek === 0 || dayOfWeek === 6) continue; // Skip weekends
    const dateStr = `${year}-${String(month).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
    if (new Date(dateStr) > new Date()) continue; // Skip future dates
    records[dateStr] = {};
    INITIAL_EMPLOYEES.forEach(emp => {
      const rand = Math.random();
      if (emp.status === 'on-leave') {
        records[dateStr][emp.id] = rand < 0.5 ? 'leave' : 'absent';
      } else {
        if (rand < 0.85) records[dateStr][emp.id] = 'present';
        else if (rand < 0.92) records[dateStr][emp.id] = 'late';
        else if (rand < 0.96) records[dateStr][emp.id] = 'leave';
        else records[dateStr][emp.id] = 'absent';
      }
    });
  }
  return records;
};

